package com.ce.service.json;

/**
 * @author sanya
 * 
 */
public class JCommunicationType {

    private String id;
    private String comType;
    private String comTypeDesc;

    /**
     * @return the id
     */
    public String getId() {
        return id;
    }

    /**
     * @param id
     *            the id to set
     */
    public void setId(String id) {
        this.id = id;
    }

    /**
     * @return the comType
     */
    public String getComType() {
        return comType;
    }

    /**
     * @param comType
     *            the comType to set
     */
    public void setComType(String comType) {
        this.comType = comType;
    }

    /**
     * @return the comDesc
     */
    public String getComTypeDesc() {
        return comTypeDesc;
    }

    /**
     * @param comDesc
     *            the comDesc to set
     */
    public void setComTypeDesc(String comDesc) {
        this.comTypeDesc = comDesc;
    }

}
