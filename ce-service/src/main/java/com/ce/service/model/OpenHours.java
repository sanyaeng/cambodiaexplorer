package com.ce.service.model;

/**
 * @author sanya
 * 
 */
public class OpenHours {
    // Day of week
    private String day;

    private String open;

    /**
     * @return the open
     */
    public String getOpen() {
        return open;
    }

    /**
     * @param open
     *            the open to set
     */
    public void setOpen(String open) {
        this.open = open;
    }

    /**
     * @return the day
     */
    public String getDay() {
        return day;
    }

    /**
     * @param day
     *            the day to set
     */
    public void setDay(String day) {
        this.day = day;
    }
}
